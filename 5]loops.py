# Range
''' Range function returns a sequence of numbers staring from 0 by default and increment by 1 (by default) and stops before a specified number
     -range(start?,stop,step?)

     e.g. =>
     for el in range(5):
          print(el)

     for el in range(1,5):
          print(el)

     for el in range(1,5,2):
          print(el)

'''

